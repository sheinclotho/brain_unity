# brain_dynamics — Unified Brain Dynamics Support Library
#
# This package contains all dynamics analysis modules merged from:
#   - twinbrain-dynamics/ (model-driven GNN dynamics analysis)
#   - spectral_dynamics/ (matrix-driven spectral analysis)
#
# Sub-packages:
#   analysis/         Core analysis algorithms (lyapunov, stability, jacobian, etc.)
#   simulator/        BrainDynamicsSimulator and stimulus classes
#   loader/           Model and graph loading utilities
#   experiments/      Experiment runners (free dynamics, attractor, stimulation, etc.)
#   visualization/    Plotting utilities
#   spectral_dynamics/ Spectral analysis experiments (E1-E6, A, C, D, F, H, I)
